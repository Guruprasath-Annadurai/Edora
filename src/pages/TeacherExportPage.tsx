// ── Types ─────────────────────────────────────────────────────────────────────

interface SubjectMastery {
  mastered: number;
  total: number;
  avg_ef: number;
  weak_topics: string[];
  strong_topics: string[];
}

interface ErrorPattern {
  subject: string;
  pattern: string;
  frequency: number;
}

interface ExportData {
  mastery_by_subject: { [sub: string]: SubjectMastery };
  error_patterns: ErrorPattern[];
  trajectory: {
    direction: 'improving' | 'stable' | 'declining';
    weekly_xp: number[];
    trend: string;
  };
  prediction: { predicted_score: number; predicted_grade: string } | null;
}

interface TeacherExport {
  id: string;
  export_html: string;
  export_data: ExportData;
  generated_at: string;
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function timeAgo(isoDate: string): string {
  const diffMs   = Date.now() - new Date(isoDate).getTime();
  const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24));
  if (diffDays === 0) return 'today';
  if (diffDays === 1) return '1 day ago';
  return `${diffDays} days ago`;
}

async function shareReport(html: string) {
  try {
    await Share.share({
      title: 'Edora Teacher Report',
      text: 'My Edora academic progress report — generated by Novo AI.',
      dialogTitle: 'Share with Teacher',
    });
    return;
  } catch { /* user cancelled or share not available — fall through */ }
  // Fallback: copy HTML to clipboard
  try {
    await navigator.clipboard.writeText(html);
    alert('Report HTML copied to clipboard — paste into an email to share.');
  } catch {
    alert('Could not share. Please take a screenshot or contact support.');
  }
}

// ── Generating steps animation ────────────────────────────────────────────────

const STEPS = [
  'Analysing your mastery data...',
  'Reviewing error patterns...',
  'Novo is writing your assessment...',
  'Formatting your report...',
];

function GeneratingSteps() {
  const [step, setStep] = useState(0);

  useEffect(() => {
    if (step >= STEPS.length - 1) return;
    const t = setTimeout(() => setStep(s => s + 1), 2000);
    return () => clearTimeout(t);
  }, [step]);

  return (
    <div className="rounded-3xl p-5"
      style={{ background: 'rgba(15,20,45,0.75)', border: '1px solid rgba(255,255,255,0.07)' }}>
      <div className="flex items-center gap-3 mb-5">
        <div className="w-10 h-10 rounded-2xl flex items-center justify-center"
          style={{ background: 'linear-gradient(135deg, #5B6AF5, #8B5CF6)' }}>
          <FileText size={18} className="text-white" />
        </div>
        <div>
          <p className="font-semibold text-white text-sm">Building your report</p>
          <p className="text-[11px]" style={{ color: 'rgba(255,255,255,0.5)' }}>This takes ~15 seconds</p>
        </div>
      </div>

      <div className="flex flex-col gap-2">
        {STEPS.map((label, i) => (
          <motion.div key={i}
            initial={{ opacity: 0.25 }}
            animate={{ opacity: i <= step ? 1 : 0.25 }}
            className="flex items-center gap-2.5">
            <motion.div
              className="w-4 h-4 rounded-full border-2 flex items-center justify-center shrink-0"
              animate={{
                borderColor: i < step ? '#10B981' : i === step ? '#5B6AF5' : 'rgba(255,255,255,0.15)',
                background:  i < step ? '#10B981' : 'transparent',
              }}>
              {i < step && (
                <svg width="8" height="8" viewBox="0 0 8 8" fill="none">
                  <path d="M1.5 4L3 5.5L6.5 2" stroke="white" strokeWidth="1.5" strokeLinecap="round" />
                </svg>
              )}
              {i === step && (
                <motion.div className="w-1.5 h-1.5 rounded-full"
                  style={{ background: '#5B6AF5' }}
                  animate={{ scale: [1, 1.4, 1] }}
                  transition={{ duration: 0.8, repeat: Infinity }} />
              )}
            </motion.div>
            <span className="text-xs" style={{ color: i === step ? '#fff' : 'rgba(255,255,255,0.5)', fontWeight: i === step ? 600 : 400 }}>
              {label}
            </span>
          </motion.div>
        ))}
      </div>
    </div>
  );
}

// ── Subject mastery bar ───────────────────────────────────────────────────────

function MasteryBar({ subject, mastered, total }: { subject: string; mastered: number; total: number }) {
  const pct   = total > 0 ? Math.round((mastered / total) * 100) : 0;
  const color = pct >= 70 ? '#10B981' : pct >= 40 ? '#F59E0B' : '#EF4444';

  return (
    <div>
      <div className="flex justify-between items-center mb-1">
        <span className="text-xs font-medium text-white capitalize">{subject}</span>
        <span className="text-[10px]" style={{ color: 'rgba(255,255,255,0.5)' }}>{mastered}/{total} · {pct}%</span>
      </div>
      <div className="h-2 rounded-full overflow-hidden" style={{ background: 'rgba(255,255,255,0.08)' }}>
        <motion.div
          className="h-full rounded-full"
          style={{ background: color }}
          initial={{ width: 0 }}
          animate={{ width: `${pct}%` }}
          transition={{ duration: 0.9, ease: 'easeOut' }} />
      </div>
    </div>
  );
}

// ── Trajectory badge ──────────────────────────────────────────────────────────

function TrajectoryBadge({ direction }: { direction: 'improving' | 'stable' | 'declining' }) {
  const map = {
    improving: { icon: TrendingUp,   label: 'Improving', bg: 'rgba(16,185,129,0.12)',  color: '#34D399' },
    stable:    { icon: Minus,        label: 'Stable',    bg: 'rgba(245,158,11,0.12)',  color: '#FBBF24' },
    declining: { icon: TrendingDown, label: 'Declining', bg: 'rgba(239,68,68,0.12)',   color: '#F87171' },
  };
  const { icon: Icon, label, bg, color } = map[direction];
  return (
    <span className="inline-flex items-center gap-1.5 px-3 py-1.5 rounded-full text-xs font-semibold"
      style={{ background: bg, color }}>
      <Icon size={13} /> {label}
    </span>
  );
}

// ── Skeleton ──────────────────────────────────────────────────────────────────

function Skeleton({ className }: { className?: string }) {
  return <div className={`animate-pulse rounded-xl ${className ?? ''}`} style={{ background: 'rgba(255,255,255,0.06)' }} />;
}

// ── Main component ────────────────────────────────────────────────────────────

