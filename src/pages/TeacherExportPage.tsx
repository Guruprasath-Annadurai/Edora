    try {
      const { data } = await supabase.functions.invoke('teacher-export', {
        body: { action: 'generate' },
      });
      if (data?.export) {
        setExportData(data.export as TeacherExport);
      }
    } catch (e) {
      console.error('[TeacherExport] generate:', e);
    } finally {
      setGenerating(false);
    }
